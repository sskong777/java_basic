package folder;

public class HelloJava {
	
	public static void main(String[] args) {
		// 주석
		// 주석은 프로그램에 영향을 주지 않는다.
		// 주석은 소스내용 설명을 위한 코드( 즉 개발자를 위한 코드)
		// 한줄 주석
		/* 범위 주석 */
		
		System.out.println("Hello~~Java!!");
		// 문자열 안에서는 주석이 될 수 없다.
		
		System.out.println("/* 문자열 안에서는 주석이 될 수 없습니다. */");
		// 이클립스에서 자주 사용하는 단축키
		// tab  : 들여쓰기
		// shift + tab : 내어쓰기
		// ctrl + shift + c : 라인 주석 단축키
		// ctrl + ( +, - )  : 폰트크기조절 단축키
		// ctrl + space : 자동완성
		// ex> sysoou 후 ctrl + space >> System.out.println();
		// ctrl + shift + f : 자동정렬
	}	

}
